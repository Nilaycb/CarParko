    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        if (!isInt(vid.getText())) {
            JOptionPane.showMessageDialog(this, "Invalid Vehicle ID!", "CarParko", JOptionPane.ERROR_MESSAGE);
        }
        else if (v_vid.contains(Integer.parseInt(vid.getText())) == true) {
            JOptionPane.showMessageDialog(this, "Vehicle ID already exists!", "CarParko", JOptionPane.ERROR_MESSAGE);
        }
        else if (!v_type_car.isSelected() && !v_type_truck.isSelected()) {
            JOptionPane.showMessageDialog(this, "Invalid Vehicle Type!", "CarParko", JOptionPane.ERROR_MESSAGE);
        }
        else if (!isInt(v_size.getText())) {
            JOptionPane.showMessageDialog(this, "Invalid Vehicle Size!", "CarParko", JOptionPane.ERROR_MESSAGE);
        }
        else if (!isInt(v_time.getText())) {
            JOptionPane.showMessageDialog(this, "Invalid Duration!", "CarParko", JOptionPane.ERROR_MESSAGE);
        }
        else {
            hm.addRecord(Integer.parseInt(vid.getText()), v_type, Integer.parseInt(v_size.getText()), Integer.parseInt(v_time.getText()));
            JOptionPane.showMessageDialog(this, "Record Successfully Added.", "CarParko", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
        }
    }//GEN-LAST:event_submitActionPerformed

