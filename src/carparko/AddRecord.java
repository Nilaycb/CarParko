    public boolean isInt(String s) {
        boolean isValidInt = false;
        
        try {
            Integer.parseInt(s);
            isValidInt = true;
        }
        catch (NumberFormatException e) {
            // exception code goes here
        }
        
        return isValidInt;
    }

