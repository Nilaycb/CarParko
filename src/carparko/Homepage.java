    private void startLoop(int f) {
       if(f == 1) {
           try {
               while (true) {
                   looper();
                   runList();
                   Thread.sleep(3 * 1000);
               }
           } catch (InterruptedException e) {
               e.printStackTrace();
           }
       }
       else {
           looper();
           runList();
       }
    }
    
