    private void runList() {
        int bestIndx = -1;
        int n = r_vid.size();
        int max_size = -1;
        int found_bigger = 0;
        int found_bigger_indx = -1;
        int i = 0;
        
        for (int j = 0; j < 10; j++) {
            if (j == 0) {
                max_size = p_block[j];
            } else if (max_size < p_block[j]) {
                max_size = p_block[j];
            }
        }
        
        if (n > 0) {
            bestIndx = -1;
            
            for (int j = 0; j < 10; j++) {
                Integer rs = new Integer(0);

                if (r_size.get(i) instanceof Integer) {
                    rs = (Integer) r_size.get(i);
                    
                    if((rs > max_size) && (max_size != -1)) {
                        found_bigger = 1;
                        found_bigger_indx = j;
                        break;
                    }
                }

                if ((p_block[j] >= rs) && (p_block_bool[j] == -1)) {
                    if (bestIndx == -1) {
                        bestIndx = j;
                    } else if (p_block[bestIndx] > p_block[j]) {
                        bestIndx = j;
                    }
                }
            }

            if (bestIndx != -1) {
                // 1 sec = 1000 ms
                long curr_time = System.currentTimeMillis();

                //remove the subpanel from GUI
                remove_waitingsGUI(i);
                    
                // allocate block bestIndx to r_vid.get(i) vehicle
                p_block_bool[bestIndx] = 1;

                if (r_vid.get(i) instanceof Integer) {
                    Integer intgr = (Integer) r_vid.get(i);
                    p_vid[bestIndx] = intgr;
                    r_vid.remove(i);
                }

                if (r_type.get(i) instanceof Integer) {
                    Integer intgr = (Integer) r_type.get(i);
                    p_type[bestIndx] = intgr;
                    r_type.remove(i);
                }

                if (r_size.get(i) instanceof Integer) {
                    Integer intgr = (Integer) r_size.get(i);
                    p_size[bestIndx] = intgr;
                    r_size.remove(i);
                }

                if (r_time.get(i) instanceof Integer) {
                    Integer intgr = (Integer) r_time.get(i);
                    p_time[bestIndx] = intgr;
                    p_tlimit[bestIndx] = curr_time + (p_time[bestIndx] * 1000);
                    r_time.remove(i);
                }
                
//                Timer timer = new Timer();
//                timer.schedule(new TimerTask() {
//                    @Override
//                    public void run() {
//                        //This is where we tell it what to do when it hits 5 seconds
//                        looper();
//                        JOptionPane.showMessageDialog(main_panel, "ADDED");
//                    }
//                }, p_time[bestIndx]);
                
                revalidateParking();
            }
            
            if((found_bigger == 1) && (found_bigger_indx > -1)) {
                remove_waitingsGUI(found_bigger_indx);
                r_vid.remove(found_bigger_indx);
                r_type.remove(found_bigger_indx);
                r_size.remove(found_bigger_indx);
                r_time.remove(found_bigger_indx);
                revalidateParking();
            }
        }
    }
    
    private void looper() {
        for(int i = 0; i < 10; i++) {
            if (p_block_bool[i] != -1) {
                if (checkValidity(i) == false) {
                    JOptionPane.showMessageDialog(main_panel, "Parking available at: "+Integer.toString(i+1));
                    p_block_bool[i] = -1;
                    p_vid[i] = 0;
                    p_type[i] = 0;
                    p_size[i] = 0;
                    p_time[i] = 0;
                    p_rmv_notified[i] = 0;
                    revalidateParking();
                }
            }
        }
        
//        revalidateParking();
    }
    
